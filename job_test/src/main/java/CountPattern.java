import java.util.HashMap;

public class CountPattern extends MainPattern{

    private HashMap<Character, Integer> pattern;
    private String patternStringForm;

//--------------------------------------------------------------------------------------------------------------------------------------------------
//
//--------------------------------------------------------------------------------------------------------------------------------------------------
    public void setPattern(HashMap<Character, Integer> pattern) {
        this.pattern = pattern;
    }

//--------------------------------------------------------------------------------------------------------------------------------------------------
//
//--------------------------------------------------------------------------------------------------------------------------------------------------
    public HashMap<Character, Integer> getPattern() {
        return pattern;
    }

//--------------------------------------------------------------------------------------------------------------------------------------------------
//
//--------------------------------------------------------------------------------------------------------------------------------------------------
    public void setPatternStringForm(String patternName)
    {
        patternStringForm = patternName;
    }

//--------------------------------------------------------------------------------------------------------------------------------------------------
//
//--------------------------------------------------------------------------------------------------------------------------------------------------
    public String getPatternStringForm() {
        return patternStringForm;
    }
}
